	<h2>About Me</h2>
	<div>
		<p>
			I'm a fullstack developer with programming experience of roughly <b>10 years</b>, born and
			raised in Germany. Due to my parents both sharing a mathematical background and proficiency in
			computer science, I suppose it's only natural that my interest for software development
			emerged at a young age.
		</p>
		<p>
			As with every journey, there's got to be a beginning - something to kick it off and set the
			ball rolling. Back in the days, my introduction to programming were graphical environments
			like
			<a href="http:////scratch.mit.edu/" target="_blank" rel="noopener noreferrer">Scratch</a>
			and
			<a
				href="https://www.generationrobots.com/blog/en/nxt-g-the-development-environment-supplied-with-lego-mindstorms-nxt-g/"
				target="_blank"
				rel="noopener noreferrer">Lego Mindstorms</a
			>. You're given a set of command bricks which you can arrange and plug together however you
			like. Some might react to input, others may return output. This seems primitive at first, but
			you'd be surprised at how much a restriction of tools can push people to think outside the
			box. I was making tiny, tiny games on a heavy old laptop and it was amazing. Seeing your
			self-coded sequence play out in front of you, responding to keypresses, exactly as designed...
			it's really satisfying. That's <b>your</b> idea thriving and unfolding right before you!
		</p>
		<p>
			At this point I'd also like to include an honorable mention which really helped my brain ease
			into programmatic problem solving. You see, when I was five years old, my dad bought a
			PlayStation 3 and with it arrived a video game I would soon be obsessed with:
			<a
				href="https://www.mediamolecule.com/games/littlebigplanet"
				target="_blank"
				rel="noopener noreferrer">Little Big Planet</a
			>, my beloved. Apart from an adorable design and story mode, players were able to make their
			own levels from the ground up and publish them for everyone to see. This collaborative and
			creative aspect only grew with the sequel by introducing circuitry, logical operators, custom
			input handling and more. It didn't feel like just a game, it was an entire
			<b>game engine</b>.
		</p>
		<p>
			Over the following years, my curiosity for information technology turned into a passionate
			hobby. I started researching and reading my way into <b>Java</b> to write plugins for a local
			Minecraft server. A few months later, I took the next big step and accommodated myself with
			industry game engine <b>Unity</b> and its variety of features. Lots of small game concepts which
			I had noted down in school I was finally able to realize, work up and build into standalone executables.
			Then, HTML, CSS, JavaScript - overall web design and development. Slowly but surely, I grew more
			confident in my hobby and it seemed to be getting easier and easier to learn a new language or framework.
			So I wandered from one small project to another, always just dipping my toes in and taking a new
			look at different areas of programming.
		</p>
		<p>
			From modern mobile applications to low level Arduino code, over prototype games drawn using
			pure Java to entire web apps written in Dart and SCSS, there's pretty much nothing I wouldn't
			care to try out in terms of project type and size.
		</p>
